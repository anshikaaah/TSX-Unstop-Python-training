# Addition
print(5 + 3)    # 8

# Subtraction
print(10 - 4)   # 6

# Multiplication
print(7 * 2)    # 14

# Division
print(8 / 2)    # 4.0 (float division)

# Floor Division
print(8 // 3)   # 2 (integer division)

# Modulus (remainder)
print(9 % 4)    # 1

# Exponentiation
print(2 ** 3)   # 8
print(5 > 3)     # True
print(5 < 3)     # False
print(5 == 5)    # True
print(5 != 4)    # True
print(6 >= 6)    # True
print(3 <= 2)    # False
# Logical AND
print(True and True)    # True
print(True and False)   # False

# Logical OR
print(True or False)    # True
print(False or False)   # False

# Logical NOT
print(not True)         # False
print(not False)        # True

# With comparisons
x = 10
y = 5
print(x > y and y > 2)       # True
print(x > y or y > 10)       # True
print(not (x < y))           # True
